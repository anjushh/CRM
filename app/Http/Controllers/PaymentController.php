     * @return \Illuminate\Http\Response
     */
    public function index(Request $request, $id = null)
    {   
        $company_id = active_company();
        $create_records = DB::table('clients')->where('company_id',$company_id)->get();

        if($id != null){
            $edit_records = (new Payment)->edit($id);
            return view('payment.all_payments', compact('$edit_records','create_records'))->with('i', ($request->input('page', 1) - 1) * 10);
        }
        else {
            return view('payment.all_payments', compact('create_records'))->with('i', ($request->input('page', 1) - 1) * 10);
        }
    }

